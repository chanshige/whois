    public static function is(string $tld): bool
    {
        // IDN（国際化ドメイン名）の場合、多くはccTLDなので無言でスルー
        if (self::isIdn($tld)) {
            return true;
        }

        if (str_contains($tld, '.')) {
            $tld = get_tld($tld);
        }

        return in_array($tld, self::$ccTLDs, true);
    }

    public static function isIdn($tld): bool
    {
        return str_contains($tld, 'xn--');
    }
}
